using System.IO;
using System.Security.Cryptography;
using System.Text;
using System.Text.Json;
using System.Text.Json.Nodes;

namespace Ostrasort;

public sealed class PatchInfo
{
    public bool Exists { get; init; }
    public string? Dir { get; init; }
    public string? ToolVersion { get; set; }
    public int ExcludedCount { get; set; }        // items the player rejected outright
    public List<string> CoveredKeys { get; } = new();
    public List<string> StaleReasons { get; } = new();
    public List<string> UnneededKeys { get; } = new();
    public bool Stale => StaleReasons.Count > 0;
    public bool Obsolete => Exists && CoveredKeys.Count == 0 && !Stale;   // conflicts resolved upstream
}

// ------------------------------------------------------------- merge plan ---

/// <summary>
/// One mod's candidate for a merge item. For a loot pool the candidate is the
/// entry string ("ItmChair01Loose=1.0x21"); for an object field the candidate
/// is a JSON value carried in <see cref="Node"/> (with Entry a compact display
/// of it). The synthetic source id "__union__" marks a computed array union.
/// </summary>
public sealed record MergeOption(string SourceId, string SourceLabel, string Entry, JsonNode? Node = null);

/// <summary>
/// One decision in a merge. For loot pools the token is an item name and each
/// option is that item's entry from one mod; for object merges the token is a
/// field name and each option is that field's value from one mod. Contested =
/// the options disagree and a decision is needed (the player's, or a fallback).
/// Excluded drops a loot item / reverts an object field to the base value.
/// </summary>
public sealed class MergeItem
{
    public required string Token { get; init; }
    public required List<MergeOption> Options { get; init; }
    public JsonNode? BaseNode { get; init; }      // object merges: the vanilla value (exclude reverts here)
    public bool IsArrayField { get; init; }       // object merges: an a* field (a union option is offered)
    public string? ChosenSourceId { get; set; }
    public bool AutoResolved { get; set; }        // decided by the headless fallback, not a person
    public bool Excluded { get; set; }            // drop (loot) / revert to vanilla (field)

    public bool Contested => Options.Select(o => o.Entry).Distinct(StringComparer.Ordinal).Count() > 1;
    public bool NeedsDecision => Contested && ChosenSourceId is null && !Excluded;
    public MergeOption Resolved =>
        Options.LastOrDefault(o => o.SourceId == ChosenSourceId) ?? Options[^1];
}

/// <summary>A colliding shop/loot pool being merged into a union.</summary>
public sealed class PoolPlan
{
    public required Collision Collision { get; init; }
    public required JsonNode BaseObject { get; init; }     // last claimant's object (non-pool fields win)
    public required List<MergeItem> LootItems { get; init; }
    public required List<MergeItem> CoItems { get; init; }
    public IEnumerable<MergeItem> AllItems => LootItems.Concat(CoItems);
}

/// <summary>A colliding non-loot object being field-merged (3-way, core = base).</summary>
public sealed class ObjectPlan
{
    public required Collision Collision { get; init; }
    public required string Type { get; init; }
    public required JsonNode BaseObject { get; init; }     // core version (clone); merged fields overlay it
    public required List<MergeItem> Fields { get; init; }  // only the fields that changed vs core
    public List<string> SchemaErrors { get; } = new();
}

public sealed class MergePlan
{
    public required List<PoolPlan> Pools { get; init; }
    public List<ObjectPlan> Objects { get; init; } = new();

    public IEnumerable<MergeItem> AllItems =>
        Pools.SelectMany(p => p.AllItems).Concat(Objects.SelectMany(o => o.Fields));
    public IEnumerable<MergeItem> Unresolved => AllItems.Where(i => i.NeedsDecision);
    public IEnumerable<MergeItem> ContestedItems => AllItems.Where(i => i.Contested);
    public bool IsEmpty => Pools.Count == 0 && Objects.Count == 0;
}

/// <summary>Outcome of generating the patch: what was merged, and any schema warnings on merged objects.</summary>
public sealed record GenerateResult(List<string> Merged, List<string> SchemaWarnings);

/// <summary>
/// Generates and maintains the "OstrasortPatch" mod: for shop-pool collisions
/// where neither mod's pool covers the other (no load order fixes those), it
/// writes a merged union pool that loads last. Items only one mod stocks are
/// always kept; items with disagreeing entries are decided by the player (the
/// resolver UI) or, headless, fall back to the later-loaded mod's entry and
/// are marked auto so the next interactive run offers them for review.
/// The folder is wholly owned by Ostrasort: never hand-edited, safe to delete,
/// regenerated by --patch, removed by --unpatch. A marker file records the
/// source pools (for staleness detection) and every decision (so they stick).
/// </summary>
public static class Patcher
{
    public const string FolderName = "OstrasortPatch";
    public const string MarkerFile = "ostrasort_patch.json";

    private static readonly JsonSerializerOptions Indented = new() { WriteIndented = true };

    /// <summary>
    /// Loot pools --patch can merge: every claimant has aLoots and some pair is
    /// a partial overlap. A pool any claimant edits via FFU precision commands
    /// (even when its aLoots itself is plain) is excluded: folding it into the
    /// union would drop that claimant's command-driven edits - those merge at
    /// load instead (FieldDiff marks the collision FfuMergedAtLoad).
    /// </summary>
    public static List<Collision> PatchableConflicts(Analysis a) =>
        a.Collisions.Where(c =>
            c.Type == "loot" &&
            // a final-say mod that loads last deliberately curates the pool - a
            // per-item union would re-add exactly the entries it removed, so it is
            // resolved by load order, not by merging
            !c.ResolvedByOrder &&
            c.Claimants.All(m => m.Claims.TryGetValue((c.Type, c.ObjName), out var l) && l is not null) &&
            c.Claimants.All(m => !m.FfuArrayEditClaims.Contains((c.Type, c.ObjName))) &&
            c.Pairs.Any(p => p.Rel == Relation.Partial)).ToList();

    /// <summary>Non-loot objects a 3-way field merge would improve on (flagged by FieldDiff).</summary>
    public static List<Collision> MergeableObjects(Analysis a) =>
        a.Collisions.Where(c => c.ObjectMergeable).ToList();

    /// <summary>Everything --patch can act on - loot pools plus mergeable objects.</summary>
    public static bool HasWork(Analysis a) => PatchableConflicts(a).Count > 0 || MergeableObjects(a).Count > 0;

    public static string ItemsHash(IEnumerable<string> loots) =>
        Convert.ToHexString(SHA256.HashData(Encoding.UTF8.GetBytes(
            string.Join("\n", loots.OrderBy(x => x, StringComparer.Ordinal)))));

    private static string SourceId(ModEntry m) => m.Dir ?? m.Raw;

    /// <summary>Per-source signature for staleness: the aLoots for a loot pool, the whole object otherwise.</summary>
    private static string SourceSig(GameEnv env, Collision c, ModEntry m, IReadOnlyList<string>? ignore)
    {
        if (m.Claims.TryGetValue((c.Type, c.ObjName), out var loots) && loots is not null)
            // include aCOs: it is part of the pool the patch merges, so a change to
            // it must invalidate the stored merge just as an aLoots change does
            return ItemsHash(m.CoClaims.TryGetValue((c.Type, c.ObjName), out var cos)
                ? loots.Concat(cos) : loots);
        if (m.Dir is null) return "";
        var obj = FieldDiff.LoadObject(Path.Combine(m.Dir, "data", c.Type), c.ObjName, ignore);
        return obj is null ? "" : ItemsHash(new[] { obj.ToJsonString() });
    }

    /// <summary>
    /// Signature of the BASE GAME's version of a merged object. The 3-way field
    /// merge overlays mod changes onto core, so a game update that changes the
    /// core object silently invalidates the merge - this hash catches that.
    /// Empty when core has no such object (loot pools merge mod-vs-mod and
    /// don't use a core base).
    /// </summary>
    private static string CoreSig(GameEnv env, Collision c, IReadOnlyList<string>? ignore)
    {
        var obj = FieldDiff.LoadObject(Path.Combine(env.CoreDataDir, c.Type), c.ObjName, ignore);
        return obj is null ? "" : ItemsHash(new[] { obj.ToJsonString() });
    }

    // ------------------------------------------------------------- inspect ---

    /// <summary>
    /// Reads the marker of an existing patch, verifies it against the current
    /// conflicts, and marks collisions it freshly covers as resolved.
    /// </summary>
    public static PatchInfo Inspect(GameEnv env, Analysis a)
    {
        var dir = Path.Combine(env.ModsDir, FolderName);
        var marker = Path.Combine(dir, MarkerFile);
        if (!File.Exists(marker))
            return new PatchInfo { Exists = false, Dir = Directory.Exists(dir) ? dir : null };

        var info = new PatchInfo { Exists = true, Dir = dir };
        JsonNode? root;
        try { root = JsonNode.Parse(File.ReadAllText(marker)); }
        catch (JsonException)
        {
            info.StaleReasons.Add("the patch marker file is unreadable - regenerate the patch");
            return info;
        }
        info.ToolVersion = root?["toolVersion"]?.GetValue<string>();

        // marker pool key -> the ordered (id, hash) sources it was generated
        // from, plus (object merges only) the hash of the core base it overlaid
        var markerPools = new Dictionary<string, (List<(string Id, string Hash)> Sources, string? CoreHash)>(StringComparer.Ordinal);
        foreach (var p in (root?["pools"] as JsonArray) ?? new JsonArray())
        {
            var key = $"{p?["type"]?.GetValue<string>()}/{p?["strName"]?.GetValue<string>()}";
            var sources = new List<(string, string)>();
            foreach (var s in (p?["sources"] as JsonArray) ?? new JsonArray())
                sources.Add((s?["id"]?.GetValue<string>() ?? "", s?["hash"]?.GetValue<string>() ?? ""));
            markerPools[key] = (sources, p?["coreHash"]?.GetValue<string>());
            if (p?["choices"] is JsonObject choices)
                info.ExcludedCount += choices.Count(kv => (kv.Value as JsonObject)?["excluded"]?.GetValue<bool>() == true);
        }

        var mergeableObjects = MergeableObjects(a);
        var current = PatchableConflicts(a).Concat(mergeableObjects).ToList();
        var patchIdx = a.Registered.FindIndex(m => m.IsPatch);
        foreach (var c in current)
        {
            // a merge only wins if the patch loads after every non-FFU mod it
            // merges - if a source loads later, its whole-object override beats
            // the merged version and "nothing lost" would be a false promise
            if (patchIdx >= 0 && c.Claimants.Any(m => !m.IsFfu && a.Registered.IndexOf(m) > patchIdx))
            {
                info.StaleReasons.Add($"{c.Key}: the patch loads before a mod it merges - " +
                                      "apply the suggested order or regenerate the patch");
                continue;
            }
            if (!markerPools.TryGetValue(c.Key, out var entry))
            {
                info.StaleReasons.Add($"new conflict {c.Key} is not covered yet");
                continue;
            }
            var now = c.Claimants.Select(m => (Id: SourceId(m), Hash: SourceSig(env, c, m, a.IgnorePatterns))).ToList();
            if (!now.SequenceEqual(entry.Sources))
            {
                info.StaleReasons.Add($"{c.Key}: a source mod's data (or the mod order) changed since generation");
                continue;
            }
            // object merges overlay core - a changed core base (game update)
            // invalidates the merge even when every source mod is unchanged.
            // Markers from older Ostrasort versions have no coreHash: skip.
            if (mergeableObjects.Contains(c) && entry.CoreHash is { Length: > 0 } coreHash
                && coreHash != CoreSig(env, c, a.IgnorePatterns))
            {
                info.StaleReasons.Add($"{c.Key}: the BASE GAME's version changed since generation (game update?) - " +
                                      "the merge overlays an outdated vanilla object");
                continue;
            }
            info.CoveredKeys.Add(c.Key);
            c.ResolvedByPatch = true;
        }
        foreach (var key in markerPools.Keys.Where(k => current.All(c => c.Key != k)))
            info.UnneededKeys.Add(key);

        return info;
    }

    // ---------------------------------------------------------------- plan ---

    /// <summary>
    /// Builds the merge plan for every patchable conflict. Prior decisions are
    /// pre-applied from the marker when the chosen source still offers exactly
    /// the same entry; anything new or changed comes back as needing a decision.
    /// With <paramref name="fresh"/>, prior decisions (including exclusions)
    /// are discarded and everything contested is asked again.
    /// </summary>
    public static MergePlan PlanMerge(GameEnv env, Analysis a, bool fresh = false)
    {
        var prior = fresh
            ? new Dictionary<string, Dictionary<string, (string, string, bool, bool)>>()
            : ReadPriorChoices(env);
        var pools = new List<PoolPlan>();

        foreach (var c in PatchableConflicts(a))
        {
            var lootOptions = new Dictionary<string, List<MergeOption>>(StringComparer.Ordinal);
            var lootOrder = new List<string>();
            var coOptions = new Dictionary<string, List<MergeOption>>(StringComparer.Ordinal);
            var coOrder = new List<string>();
            JsonNode? baseObj = null;

            foreach (var m in c.Claimants)
            {
                var obj = LoadPoolObject(m.Dir!, c.Type, c.ObjName, a.IgnorePatterns);
                if (obj is null) continue;
                baseObj = obj;                       // last claimant's object = base
                Collect(obj["aLoots"], m, lootOrder, lootOptions);
                Collect(obj["aCOs"], m, coOrder, coOptions);
            }
            if (baseObj is null) continue;

            prior.TryGetValue(c.Key, out var priorChoices);
            var plan = new PoolPlan
            {
                Collision = c,
                BaseObject = baseObj,
                LootItems = lootOrder.Select(t => BuildItem(t, lootOptions[t], priorChoices)).ToList(),
                // aCOs choices are stored under a "co:" prefix so a token present
                // in both lists never shares one stored decision with aLoots
                CoItems = coOrder.Select(t => BuildItem(t, coOptions[t], priorChoices, CoPrefix)).ToList(),
            };
            pools.Add(plan);
        }

        // non-loot object merges (3-way field merge, core = base)
        var objects = new List<ObjectPlan>();
        foreach (var c in MergeableObjects(a))
        {
            // a mod-added object has no vanilla ancestor - two-way merge it
            // against an empty base (fields only one mod sets are kept)
            var coreObj = FieldDiff.LoadObject(Path.Combine(env.CoreDataDir, c.Type), c.ObjName, a.IgnorePatterns);
            var versions = c.Claimants.Where(m => m.Dir is not null)
                .Select(m => (Mod: m, Obj: FieldDiff.LoadObject(Path.Combine(m.Dir!, "data", c.Type), c.ObjName, a.IgnorePatterns)))
                .Where(x => x.Obj is not null)
                .Select(x => (x.Mod, x.Obj!))
                .ToList();
            var plan = ObjectMerge.Build(c, coreObj ?? new JsonObject(), versions, a.Ffu is { FrameworkPresent: true });
            if (plan is null) continue;

            prior.TryGetValue(c.Key, out var priorChoices);
            foreach (var f in plan.Fields) ApplyPrior(f, priorChoices);
            objects.Add(plan);
        }

        return new MergePlan { Pools = pools, Objects = objects };
    }

    private static void Collect(JsonNode? array, ModEntry source, List<string> order,
                                Dictionary<string, List<MergeOption>> options)
    {
        if (array is not JsonArray ja) return;
        foreach (var n in ja)
        {
            if (n?.GetValueKind() != JsonValueKind.String) continue;
            var entry = n.GetValue<string>();
            var token = entry.Split('=')[0];
            if (!options.TryGetValue(token, out var list))
            {
                options[token] = list = new List<MergeOption>();
                order.Add(token);
            }
            list.Add(new MergeOption(SourceId(source), source.DisplayName ?? source.Name, entry));
        }
    }

    /// <summary>Marker key prefix separating aCOs decisions from aLoots decisions in a pool.</summary>
    private const string CoPrefix = "co:";

    /// <summary>
    /// Full-fidelity signature of an option's value, used to decide whether a
    /// stored decision still matches. Object-field options carry their value in
    /// Node (Entry is a truncated display string - two different values can
    /// render identically); loot options carry the entry string itself.
    /// </summary>
    private static string ValueSig(MergeOption o) => o.Node?.ToJsonString() ?? o.Entry;

    private static MergeItem BuildItem(string token, List<MergeOption> options,
                                       Dictionary<string, (string Source, string Entry, bool Auto, bool Excluded)>? prior,
                                       string prefix = "")
    {
        var item = new MergeItem { Token = token, Options = options };
        ApplyPrior(item, prior, prefix);
        return item;
    }

    /// <summary>Re-applies a stored decision (source pick or exclusion) to an item if still valid.</summary>
    private static void ApplyPrior(MergeItem item,
                                   Dictionary<string, (string Source, string Entry, bool Auto, bool Excluded)>? prior,
                                   string prefix = "")
    {
        if (prior is null) return;
        // older markers stored aCOs decisions unprefixed - fall back for them
        if (!prior.TryGetValue(prefix + item.Token, out var p) &&
            (prefix.Length == 0 || !prior.TryGetValue(item.Token, out p))) return;
        if (p.Excluded)
        {
            item.Excluded = true;               // intent about the item/field - survives value changes
            item.ChosenSourceId = null;
        }
        else if (item.Contested && item.Options.Any(o => o.SourceId == p.Source && ValueSig(o) == p.Entry))
        {
            item.ChosenSourceId = p.Source;     // decision still valid - keep it
            item.AutoResolved = p.Auto;
        }
    }

    /// <summary>
    /// Headless fallback: unresolved contested items take the later-loaded MOD's
    /// entry (matching the game's own last-wins outcome), marked auto. The
    /// synthetic union option is never auto-picked - a computed union the game
    /// would not itself produce must be a person's explicit choice.
    /// </summary>
    public static void ResolveFallback(MergePlan plan)
    {
        foreach (var item in plan.Unresolved.ToList())
        {
            var pick = item.Options.LastOrDefault(o => o.SourceId != "__union__") ?? item.Options[^1];
            item.ChosenSourceId = pick.SourceId;
            item.AutoResolved = true;
        }
    }

    private static Dictionary<string, Dictionary<string, (string Source, string Entry, bool Auto, bool Excluded)>> ReadPriorChoices(GameEnv env)
    {
        var result = new Dictionary<string, Dictionary<string, (string, string, bool, bool)>>(StringComparer.Ordinal);
        var marker = Path.Combine(env.ModsDir, FolderName, MarkerFile);
        if (!File.Exists(marker)) return result;
        JsonNode? root;
        try { root = JsonNode.Parse(File.ReadAllText(marker)); }
        catch (JsonException) { return result; }

        foreach (var p in (root?["pools"] as JsonArray) ?? new JsonArray())
        {
            var key = $"{p?["type"]?.GetValue<string>()}/{p?["strName"]?.GetValue<string>()}";
            if (p?["choices"] is not JsonObject choices) continue;
            var map = new Dictionary<string, (string, string, bool, bool)>(StringComparer.Ordinal);
            foreach (var (token, val) in choices)
            {
                if (val is not JsonObject o) continue;
                map[token] = (
                    o["source"]?.GetValue<string>() ?? "",
                    o["entry"]?.GetValue<string>() ?? "",
                    o["auto"]?.GetValue<bool>() ?? false,
                    o["excluded"]?.GetValue<bool>() ?? false);
            }
            result[key] = map;
        }
        return result;
    }

    // ------------------------------------------------------------ generate ---

    /// <summary>
    /// Writes the patch mod from a fully resolved plan and registers it last -
    /// or, when FFU mods are present, at the end of the non-FFU block (FFU mods
    /// intentionally load after it and field-merge on top).
    /// </summary>
    public static GenerateResult Generate(GameEnv env, MergePlan plan, Analysis a, string? installedVersion, string toolVersion)
    {
        if (plan.IsEmpty)
            throw new InvalidOperationException("nothing to patch - there are no mergeable conflicts.");
        if (plan.Unresolved.Any())
            throw new InvalidOperationException("the merge plan still has undecided items - resolve them first.");

        var dir = Path.Combine(env.ModsDir, FolderName);
        if (Directory.Exists(dir) && !File.Exists(Path.Combine(dir, MarkerFile))
            && Directory.EnumerateFileSystemEntries(dir).Any())
            throw new InvalidOperationException(
                $"'{dir}' exists but has no {MarkerFile} - refusing to overwrite a folder Ostrasort did not generate.");

        // a previous generation's data files must not survive: a conflict class
        // that disappeared (a mod uninstalled or updated) would otherwise keep
        // overriding the game via leftover files no staleness check can see
        var dataDir = Path.Combine(dir, "data");
        if (Directory.Exists(dataDir)) Directory.Delete(dataDir, recursive: true);

        var validator = new SchemaValidator(env);
        var markerPools = new JsonArray();
        var descriptions = new List<string>();
        var schemaWarnings = new List<string>();

        JsonArray SourcesFor(Collision c)
        {
            var arr = new JsonArray();
            foreach (var m in c.Claimants)
                arr.Add(new JsonObject { ["id"] = SourceId(m), ["label"] = m.Label, ["hash"] = SourceSig(env, c, m, a.IgnorePatterns) });
            return arr;
        }
        void AddChoices(JsonObject choices, IEnumerable<MergeItem> items, string prefix = "")
        {
            foreach (var i in items.Where(i => i.Excluded))
                choices[prefix + i.Token] = new JsonObject { ["excluded"] = true };
            foreach (var i in items.Where(i => i.Contested && !i.Excluded))
                choices[prefix + i.Token] = new JsonObject
                {
                    ["source"] = i.Resolved.SourceId,
                    ["entry"] = ValueSig(i.Resolved),
                    ["auto"] = i.AutoResolved,
                };
        }
        JsonObject ChoicesFor(IEnumerable<MergeItem> items)
        {
            var choices = new JsonObject();
            AddChoices(choices, items);
            return choices;
        }

        // ---- loot pools -> data/loot/loot.json (per-item union) ----
        if (plan.Pools.Count > 0)
        {
            Directory.CreateDirectory(Path.Combine(dir, "data", "loot"));
            var poolNodes = new JsonArray();
            foreach (var p in plan.Pools)
            {
                var obj = p.BaseObject;
                obj["aLoots"] = new JsonArray(p.LootItems.Where(i => !i.Excluded).Select(i => (JsonNode)i.Resolved.Entry).ToArray());
                // don't invent an aCOs field no source pool carried
                if (p.CoItems.Count > 0 || obj["aCOs"] is not null)
                    obj["aCOs"] = new JsonArray(p.CoItems.Where(i => !i.Excluded).Select(i => (JsonNode)i.Resolved.Entry).ToArray());
                poolNodes.Add(obj);
                var poolChoices = new JsonObject();
                AddChoices(poolChoices, p.LootItems);
                AddChoices(poolChoices, p.CoItems, CoPrefix);
                markerPools.Add(new JsonObject
                {
                    ["type"] = p.Collision.Type,
                    ["strName"] = p.Collision.ObjName,
                    ["kind"] = "loot",
                    ["sources"] = SourcesFor(p.Collision),
                    ["choices"] = poolChoices,
                });
                descriptions.Add($"{p.Collision.ObjName} = {string.Join(" + ", p.Collision.Claimants.Select(m => m.DisplayName ?? m.Name))}");
            }
            File.WriteAllText(Path.Combine(dir, "data", "loot", "loot.json"), poolNodes.ToJsonString(Indented) + "\n");
        }

        // ---- object merges -> data/<type>/ostrasort_merged.json (field merge, schema-validated) ----
        foreach (var byType in plan.Objects.GroupBy(o => o.Type))
        {
            Directory.CreateDirectory(Path.Combine(dir, "data", byType.Key));
            var arr = new JsonArray();
            foreach (var o in byType)
            {
                var merged = ObjectMerge.Assemble(o);
                var errs = validator.Validate(merged, o.Type);
                if (errs.Count > 0)
                {
                    o.SchemaErrors.AddRange(errs);
                    schemaWarnings.Add($"{o.Type}/{o.Collision.ObjName}: " +
                        string.Join("; ", errs.Take(3)) + (errs.Count > 3 ? $" (+{errs.Count - 3} more)" : ""));
                }
                arr.Add(merged);

                var marker = new JsonObject
                {
                    ["type"] = o.Type,
                    ["strName"] = o.Collision.ObjName,
                    ["kind"] = "object",
                    ["sources"] = SourcesFor(o.Collision),
                    // the merge overlays core - record which core version it overlaid
                    // so a game update that changes it marks the patch stale
                    ["coreHash"] = CoreSig(env, o.Collision, a.IgnorePatterns),
                    ["choices"] = ChoicesFor(o.Fields),
                };
                if (o.SchemaErrors.Count > 0)
                    marker["schemaErrors"] = new JsonArray(o.SchemaErrors.Select(e => (JsonNode)e).ToArray());
                markerPools.Add(marker);

                var kept = o.Fields.Count(f => !f.Excluded);
                descriptions.Add($"{o.Collision.ObjName} (merged {kept} field(s))");
            }
            File.WriteAllText(Path.Combine(dir, "data", byType.Key, "ostrasort_merged.json"), arr.ToJsonString(Indented) + "\n");
        }

        var poolCount = plan.Pools.Count;
        var objCount = plan.Objects.Count;
        var summary = string.Join(" and ", new[]
        {
            poolCount > 0 ? $"{poolCount} shop pool(s)" : null,
            objCount > 0 ? $"{objCount} object(s)" : null,
        }.Where(x => x != null));

        var modInfo = new JsonArray(new JsonObject
        {
            ["strName"] = "Ostrasort Patch",
            ["strAuthor"] = "Ostrasort",
            ["strModURL"] = "",
            ["strGameVersion"] = installedVersion ?? "",
            ["strModVersion"] = toolVersion,
            ["strNotes"] = $"Auto-generated by Ostrasort. Merges {summary} that conflicting mods both " +
                           "override, so no mod's changes are lost: " + string.Join("; ", descriptions) +
                           ". Must load after every mod it merges (last, or right before the FFU block). " +
                           "Do not edit by hand - re-run Ostrasort's patch after any " +
                           "of the merged mods update, or remove it with --unpatch / the Remove button.",
            ["strWorkshopID"] = "",
        });
        File.WriteAllText(Path.Combine(dir, "mod_info.json"), modInfo.ToJsonString(Indented) + "\n");

        var markerRoot = new JsonObject
        {
            ["toolVersion"] = toolVersion,
            ["generatedUtc"] = DateTime.UtcNow.ToString("o"),
            ["pools"] = markerPools,
        };
        // the marker holds the player's merge decisions - never risk truncating it
        AtomicFile.WriteAllText(Path.Combine(dir, MarkerFile), markerRoot.ToJsonString(Indented) + "\n");

        EnsureRegistered(env, a);
        return new GenerateResult(descriptions, schemaWarnings);
    }

    /// <summary>
    /// Finds one object by strName in a mod's data\&lt;type&gt; folder and
    /// deep-clones it. Delegates to <see cref="FieldDiff.LoadObject"/> so the
    /// pool merge reads the exact same object the field diff does.
    /// </summary>
    private static JsonNode? LoadPoolObject(string modDir, string type, string strName, IReadOnlyList<string>? ignorePatterns = null) =>
        FieldDiff.LoadObject(Path.Combine(modDir, "data", type), strName, ignorePatterns)?.DeepClone();

    // -------------------------------------------------- register / remove ---

    private static void EnsureRegistered(GameEnv env, Analysis a)
    {
        var lo = LoadOrderFile.Read(env.LoadingOrderPath);
        var target = lo.Order.Where(e => e.Split('|')[0] != FolderName).ToList();   // drop any marker variant

        // the patch must load after every non-FFU mod it merges; FFU mods keep
        // loading after it (they field-merge on top at load). Anchor on the
        // LAST non-FFU entry, not the first FFU one: if the current order has
        // an FFU mod interleaved early, inserting before it would put the patch
        // ahead of mods it merges and their whole-object overrides would win.
        var ffuRaws = a.Registered.Where(m => m.IsFfu && m.Raw.Length > 0)
            .Select(m => m.Raw).ToHashSet(StringComparer.OrdinalIgnoreCase);
        var lastNonFfu = target.FindLastIndex(e => !ffuRaws.Contains(e));
        if (lastNonFfu < 0) target.Add(FolderName);
        else target.Insert(lastNonFfu + 1, FolderName);

        if (!target.SequenceEqual(lo.Order)) lo.Write(target);
    }

    public static void Remove(GameEnv env)
    {
        var dir = Path.Combine(env.ModsDir, FolderName);
        if (Directory.Exists(dir))
        {
            if (!File.Exists(Path.Combine(dir, MarkerFile)))
                throw new InvalidOperationException(
                    $"'{dir}' has no {MarkerFile} - refusing to delete a folder Ostrasort did not generate.");
            Directory.Delete(dir, recursive: true);
        }
        var lo = LoadOrderFile.Read(env.LoadingOrderPath);
        var target = lo.Order.Where(e => e.Split('|')[0] != FolderName).ToList();   // drop any marker variant
        if (target.Count != lo.Order.Count) lo.Write(target);
    }
